// Generate 90-day sales lift series — observed / expected / incremental
function salesLiftSeries(seed) {
  const hash = seed.split('').reduce((a, c) => a + c.charCodeAt(0), 0)
  const rng = (i) => ((hash * 9301 + i * 49297 + 233280) % 233280) / 233280

  return Array.from({ length: 12 }, (_, i) => {
    const week = i + 1
    const baseExpected = 80000 + rng(i * 3) * 40000
    const liftFactor = 1 + (week / 14) * (0.15 + rng(i * 7) * 0.12)
    const observed = baseExpected * liftFactor
    const incremental = observed - baseExpected
    return {
      week: `W${week}`,
      observed: Math.round(observed),
      expected: Math.round(baseExpected),
      incremental: Math.round(incremental),
    }
  })
}

